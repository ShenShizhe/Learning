/*
():左结合，优先级：1
++:右结合，优先级：2
+：左结合，优先级：4
=：右结合，优先级：14
*/
int main_85_2(void)
{
	int i = 2, z;
	z = (i++) + (i++) + (i++);

	printf("%d %d", z, i);

	return 0;
}


/*
按照当前的C语言语法[https://learn.microsoft.com/zh-cn/cpp/c-language/?view=msvc-170]规定：

运算顺序：先执行(i++)，然后再依次相加，最后赋值给z


1.即每个()内的表达式优先级一致，由于自增的对象均为同一变量，所以变量的值会受前自增/自减的影响(后自增/自减在同一表达式内暂1不受影响，相关变量的值暂时不变)

2.当出现一个前自增/自减时，所有变量值均进行前自增/自减（优先级一致）。

3.在进行后自增/自减时参与的运算时，实际参与运算变量的值不变，真实值已随着表达式运算中的自增/自减以及表达式是结合性而逐步改变

4.不同的编译器的版本不同，处理运算符优先级的方式有差异，个别编译器会将后自增/自减的值作为参与运算的值

*/




